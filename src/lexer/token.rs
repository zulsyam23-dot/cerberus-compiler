use crate::error::Span;

#[derive(Debug, Clone)]
pub enum TokenKind {
    Program,
    Var,
    Begin,
    End,
    If,
    Then,
    Else,
    While,
    Do,
    Procedure,
    Function,
    Return,
    Uses,
    Array,
    Of,
    Integer,
    Boolean,
    String,
    Option,
    Result,
    Vector,
    List,
    Stack,
    Map,
    Set,
    True,
    False,
    Writeln,
    Readln,
    Semi,
    Colon,
    Comma,
    Dot,
    Assign,
    Plus,
    Minus,
    Star,
    Slash,
    Eq,
    Ne,
    Lt,
    Le,
    Gt,
    Ge,
    And,
    Or,
    Not,
    LParen,
    RParen,
    LBracket,
    RBracket,
    Ident(String),
    IntLit(i64),
    StringLit(String),
    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}
